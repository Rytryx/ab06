function sum(a, b) {
   const result = a + b;
   return result;
}
function sub(a, b) {
    const result = a - b;
    return result;
}
function mul(a, b) {
    const result = a * b;
    return result;
}
function div(a, b) {
    const result = a / b;
    return result;
}
module.exports = { sum, sub, mul, div };